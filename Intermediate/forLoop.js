const days = ['MON','TUE','WED','THU','FRI','SAT','SUN']

// Ascending Odrer
console.log("Ascending Odrer");

for (let i = 0; i < days.length; i++) {  
    console.log(days[i]);
}

// Descending Odrer
console.log("Descending Odrer");
for (let i = days.length-1; i >= 0 ; i--) {  
    console.log(days[i]);
}
